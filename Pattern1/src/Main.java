public class Main {
    public static void main(String[] args) {
        int row = 5;
        System.out.println("Print Pattern##");
        for (int i = 1; i <= row; i++)
        {
            for (int j = 1; j <= i; j++)
            {
                System.out.println("1 ");
            }
            System.out.println();
        }
    }
}